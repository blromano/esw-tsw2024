package conecta.entidades;

public class Servicos {

    private int id_servicos;
    private String ser_nome;
    private String ser_area;
    private String ser_descricao;

    public int getId_servicos() {
        return id_servicos;
    }

    public void setId_servicos(int id_servicos) {
        this.id_servicos = id_servicos;
    }

    public String getSer_nome() {
        return ser_nome;
    }

    public void setSer_nome(String ser_nome) {
        this.ser_nome = ser_nome;
    }

    public String getSer_area() {
        return ser_area;
    }

    public void setSer_area(String ser_area) {
        this.ser_area = ser_area;
    }

    public String getSer_descricao() {
        return ser_descricao;
    }

    public void setSer_descricao(String ser_descricao) {
        this.ser_descricao = ser_descricao;
    }

}
